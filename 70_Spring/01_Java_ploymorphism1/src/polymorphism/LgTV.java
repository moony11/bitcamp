package polymorphism;

public class LgTV {
	public void on() {
			System.out.println("LgTV - 전원ON");
	}
	public void off() {
		System.out.println("LgTV - 전원off");
}
	public void soundUp() {
		System.out.println("LgTV - 소리 크게~~~");
}
	public void soundDown() {
		System.out.println("LgTV - 소리 작게~~~");
}
}
